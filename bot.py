@dp.callback_query()
async def handle_callbacks(callback: types.CallbackQuery):
    data = callback.data.split(":")
    action = data[0]

    # –æ—Ç–∫—Ä—ã—Ç—å –º–µ–Ω—é —Å–µ—Ä–∏–π
    if action == "series_menu":
        _, code, page = data
        total = len(series[code]["episodes"])
        await callback.message.edit_reply_markup(
            reply_markup=series_menu_keyboard(code, total, int(page))
        )

    if action in ("prev", "next"):
        _, code, episode = data
        episode = int(episode)

        if action == "prev":
            episode -= 1
        else:
            episode += 1

        await send_episode(callback, code, episode)

        await callback.answer()
        return



    # –ø–µ—Ä–µ–ª–∏—Å—Ç—ã–≤–∞–Ω–∏–µ —Å—Ç—Ä–∞–Ω–∏—Ü
    elif action == "page":
        _, code, page = data
        total = len(series[code]["episodes"])
        await callback.message.edit_reply_markup(
            reply_markup=series_menu_keyboard(code, total, int(page))
        )

    # –≤—ã–±–æ—Ä —Å–µ—Ä–∏–∏ ‚Üí –í–ö–õ–Æ–ß–ê–ï–ú –í–ò–î–ï–û
    elif action == "ep":
        _, code, episode = data
        await send_episode(callback, code, int(episode))

    # –Ω–∞–∑–∞–¥ –∫ –∫–∞—Ä—Ç–æ—á–∫–µ —Å–µ—Ä–∏–∞–ª–∞
    elif action == "serial":
        _, code = data
        await callback.message.delete()
        await send_serial_card(callback.message, code)

    elif action == "menu":
        _, code, page = data
        total = len(series[code]["episodes"])

        await callback.message.edit_reply_markup(
            reply_markup=series_menu_keyboard(code, total, int(page))
    )

    await callback.answer()

