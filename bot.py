def series_menu_keyboard(code: str, total: int, page: int = 0):
    per_page = 10
    start = page * per_page
    end = min(start + per_page, total)

    keyboard = []
    row = []

    for i in range(start, end):
        row.append(
            InlineKeyboardButton(
                text=str(i + 1),
                callback_data=f"ep:{code}:{i}"
            )
        )
        if len(row) == 5:
            keyboard.append(row)
            row = []

    if row:
        keyboard.append(row)

    nav = []
    if page > 0:
        nav.append(
            InlineKeyboardButton(text="‚¨ÖÔ∏è", callback_data=f"page:{code}:{page-1}")
        )

    if end < total:
        nav.append(
            InlineKeyboardButton(text="‚û°Ô∏è", callback_data=f"page:{code}:{page+1}")
        )

    keyboard.append(nav)
    return InlineKeyboardMarkup(inline_keyboard=keyboard)



